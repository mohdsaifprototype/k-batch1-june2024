/* 
Things that we can do with numbers
*/

let a = 5,
  b = 2,
  c;

// Addition
c = a + b;
console.log(a, "+", b, "=", c);
// Subtraction
c = a - b;
console.log(a, "-", b, "=", c);
// Division
c = a / b;
console.log(a, "/", b, "=", c);
// Multiplication
c = a * b;
console.log(a, "*", b, "=", c);
// Modulus (Remainder)
c = a % b;
console.log(a, "%", b, "=", c);
// Exponentiation (Power)
c = a ** b;
console.log(a, "**", b, "=", c);
